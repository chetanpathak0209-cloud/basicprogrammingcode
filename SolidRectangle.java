import java.util.*;

public class SolidRectangle {
    public static void main(String[] args){
        System.out.println("Solid Rectangle Pattern:");

        int n=3;
        for(int i=0;i<n;i++){
            for(int j=0;j<5;j++){
                System.out.print("* ");
            }
            System.out.println();
        }
    }
    
}
